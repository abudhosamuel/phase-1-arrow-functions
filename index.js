// Function expression called divide
const divide = (a, b) => {
    return a / b;
  };
  
  // Arrow function called square
  const square = x => {
    return x * x;
  };
  
  // Arrow function called add
  const add = (a, b) => {
    return a + b;
  };
  
  // Testing the functions manually
  console.log(divide(2000, 100)); // Should output: 20
  console.log(square(2)); // Should output: 4
  console.log(add(3, 4)); // Should output: 7
  